package com.tatiana.operationsandcyclicfuctions;

public class AritmeticOperationExamples {
    public static void main(String[] args) {
        int a = 43;
        int c = 3;

        //Sum
        int sum = a + c;
        System.out.println("Suma:" + sum);
        System.out.println("Suma:" + (a + c));

        //Substruction
        int sub = a - c;
        System.out.println("Rezultatul:" + sub);
        System.out.println("Rezultatul:" + (a-c));

        //Multiplication
        int result = a * c;
        System.out.println("Inmultirea:" + result);
        System.out.println("Inmultirea:" + (a*c));

        //Devision
        result = a / c;
        System.out.println("Impartirea:" + result);
        System.out.println("Impartirea:" + (a/c));

        //Remainder of devision
        result = a % c;
        System.out.println("Impartirea:" + result);
        System.out.println("Impartirea:" + (a%c));

    }

}
